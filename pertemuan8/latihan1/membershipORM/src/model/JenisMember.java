package model;  

public class JenisMember {  
    private String id;  
    private String nama;  

    public void setId(String id) {  
        this.id = id;  
    }  

    public String getId() {  
        return id;  
    }  

    public void setNama(String nama) {  
        this.nama = nama;  
    }  

    public String getNama() {  
        return nama;  
    }  
}